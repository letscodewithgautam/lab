package feb_2_23;

public class average_of_number {
public static void main(String[] args) {
	
	int x1= 10;   // x1 is first number
	int x2= 20;   // x2 is second number
	int x3 = 30;  // x3 is third number
	int x4 = 40;  // x4 is fourth number
	int x5 = 50;  // x5 is fifth number
	
	int y = (x1 + x2 +x3 + x4 + x5)/5;  // y is average of 5 number 
	                                   //  sum of total number/total number
	System.out.println("The average of number is : "+y);
}
}
